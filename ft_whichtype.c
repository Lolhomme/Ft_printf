#include "ft_printf.h"

int			ft_whichtype(const char* s, int i, va_list ap)
{
	if (s[i] == 'c')
		return (istype_c(ap));
	if (s[i] == '%')
	{
		ft_putchar('%');
		return (1);
	}
}
