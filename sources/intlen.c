#include "includes/ft_printf.h"

int			intlen(long long int nb)
{
	int		i;

	i = 1;
	if (nb == 0)
		return (1);
	if (nb < 0)
		nb = -nb;
	while (nb > 9)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
